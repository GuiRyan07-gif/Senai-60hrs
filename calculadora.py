print("Digite o primeiro número:")
a = float(input())

print("Digite o segundo número:")
b = float(input())

print("Digite a operação (+  -  *  /):")
op = input()

print("Resultado:")

if op == "+":
    print(a + b)

if op == "-":
    print(a - b)

if op == "*":
    print(a * b)

if op == "/":
    print(a / b)
